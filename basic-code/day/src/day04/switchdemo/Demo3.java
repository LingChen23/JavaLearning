package day04.switchdemo;

public class Demo3 {
    public static void main(String[] args) {
        int number = 5;

        switch (number){
            case 1->{
                System.out.println("1");
            }
            case 2->{
                System.out.println("2");
            }
            case 3->{
                System.out.println("3");
            }
            default -> System.out.println("其他");

        }
    }
}
